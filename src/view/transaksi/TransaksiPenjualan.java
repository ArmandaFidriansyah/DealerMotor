    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
    if (txtNoTransaksi.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Pilih data di tabel dulu!");
        return;
    }
    int konfirmasi = JOptionPane.showConfirmDialog(this,
        "Yakin hapus transaksi " + txtNoTransaksi.getText() + "?",
        "Konfirmasi", JOptionPane.YES_NO_OPTION);
    if (konfirmasi == JOptionPane.YES_OPTION) {
        try {
            Connection conn = Koneksi.getKoneksi();
            PreparedStatement pst = conn.prepareStatement(
                "DELETE FROM transaksi_penjualan WHERE no_transaksi = ?");
            pst.setString(1, txtNoTransaksi.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
            tampilData();
            generateNoTransaksi();
            txtBayar.setText("");
            txtKembalian.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal hapus: " + e.getMessage());
        }
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnHapusActionPerformed

