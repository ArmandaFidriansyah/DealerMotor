    public void generateNoTransaksi() {
    try {
        Connection conn = Koneksi.getKoneksi();
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(
            "SELECT COUNT(*) AS total FROM transaksi_penjualan");
        if (rs.next()) {
            int total = rs.getInt("total") + 1;
            txtNoTransaksi.setText(String.format("TRX%04d", total));
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal generate no transaksi: " + e.getMessage());
    }
}
    
    public void loadNoRangka() {
    try {
        Connection conn = Koneksi.getKoneksi();
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery("SELECT no_rangka FROM penjualan_unit");
        cmbNoRangka.removeAllItems();
        while (rs.next()) {
            cmbNoRangka.addItem(rs.getString("no_rangka"));
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal load data: " + e.getMessage());
    }
}

public void loadDetailUnit() {
    try {
        Connection conn = Koneksi.getKoneksi();
        PreparedStatement pst = conn.prepareStatement(
            "SELECT * FROM penjualan_unit WHERE no_rangka = ?");
        pst.setString(1, cmbNoRangka.getSelectedItem().toString());
        ResultSet rs = pst.executeQuery();
        if (rs.next()) {
            txtTipe.setText(rs.getString("tipe"));
            txtPembeli.setText(rs.getString("pembeli"));
            txtHarga.setText(String.valueOf(rs.getString("harga")));
            txtKembalian.setText(""); // reset kembalian
            txtBayar.setText("");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal load detail: " + e.getMessage());
    }
}

    public void tampilData() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);
    try {
        Connection conn = Koneksi.getKoneksi();
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery("SELECT * FROM transaksi_penjualan");
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("no_transaksi"),
                rs.getString("tanggal"),
                rs.getString("no_rangka"),
                rs.getString("tipe"),
                rs.getString("pembeli"),
                String.format("%,.0f", rs.getDouble("harga")),
                String.format("%,.0f", rs.getDouble("bayar")),
                String.format("%,.0f", rs.getDouble("kembalian"))
            });
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal tampil data: " + e.getMessage());
    }
}
    
    
    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
         try {
        double harga = Double.parseDouble(txtHarga.getText());
        double bayar = Double.parseDouble(txtBayar.getText());

        if (bayar < harga) {
            JOptionPane.showMessageDialog(this, "Uang bayar kurang!");
            return;
        }

        double kembalian = bayar - harga;
        txtKembalian.setText(String.format("%.0f", kembalian));

        Connection conn = Koneksi.getKoneksi();
        String sql = "INSERT INTO transaksi_penjualan "
            + "(no_transaksi, tanggal, no_rangka, tipe, pembeli, harga, bayar, kembalian) "
            + "VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, txtNoTransaksi.getText());
        pst.setString(2, txtTanggal.getText());
        pst.setString(3, cmbNoRangka.getSelectedItem().toString());
        pst.setString(4, txtTipe.getText());
        pst.setString(5, txtPembeli.getText());
        pst.setDouble(6, harga);
        pst.setDouble(7, bayar);
        pst.setDouble(8, kembalian);
        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "Transaksi berhasil disimpan!");
        tampilData();
        generateNoTransaksi();
        txtBayar.setText("");
        txtKembalian.setText("");

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal simpan: " + e.getMessage());
    }// TODO add your handling code here:
    }//GEN-LAST:event_btnSimpanActionPerformed

